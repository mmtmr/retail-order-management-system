    private void buttonContinueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonContinueActionPerformed
        try {
            if (!checkBoxSave.isSelected()) {
                throw new Exception("Terms and Condition is not agreed.");
            }
            String name = textNameOnCard1.getText();
            String number = textCardNumber1.getText();
            String year = textExpYear.getText();
            String month = (String) comboExpMonth1.getSelectedItem();
            String cvv = String.valueOf(passwordCVV.getPassword());
            DateTimeFormatter formatter = new DateTimeFormatterBuilder().parseCaseInsensitive().appendPattern("MMM-yyyy").toFormatter();
            YearMonth ym = YearMonth.parse(month + "-" + year, formatter);
            LocalDate exp = ym.atDay(1);
            String bank=Validation.validateCreditCard(name, number, cvv, exp);
            order.setOrdPayment(new Payment(name,number,bank));
            order.setOrdStatus(OrderStatus.ToShip);
            rewriteOrderData();
            JOptionPane.showMessageDialog(null,"Payment success!");
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_buttonContinueActionPerformed

