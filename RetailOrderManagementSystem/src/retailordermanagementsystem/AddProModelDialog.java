    private void buttonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSaveActionPerformed
         try{
             String modelname = textModel.getText();
            int stock = (Integer) spinnerStock.getValue();
            for(ProModel pm:product.getProModels()){
                if(pm.getPMName().equals(modelname)){
                    throw new Exception("Model already existed!");
                }
            }
            
//            AccList.add(cus.getCusAccount());
//            CusList.add(cus);

            product.getProModels().add(new ProModel(modelname, stock));
            JOptionPane.showMessageDialog(null, "Model added.");
            Operation.rewriteProductData();
            this.model.addRow(new Object[]{product.getProID(), product.getProCategory(), product.getProName(), product.getProPrice(), product.getProModels().get(0).getPMName(), product.getProModels().get(0).getPMStock(), product.getProWeight(), product.isProFragile(), Supplier.searchSupFromProID(product.getProID()).getSupName()});     
            this.dispose();
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_buttonSaveActionPerformed

